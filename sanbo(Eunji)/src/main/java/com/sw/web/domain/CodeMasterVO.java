package com.sw.web.domain;

public class CodeMasterVO {
	private int id;
	private String reprec;
	private String reprec_n;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getReprec() {
		return reprec;
	}
	public void setReprec(String reprec) {
		this.reprec = reprec;
	}
	public String getReprec_n() {
		return reprec_n;
	}
	public void setReprec_n(String reprec_n) {
		this.reprec_n = reprec_n;
	}

	
}
