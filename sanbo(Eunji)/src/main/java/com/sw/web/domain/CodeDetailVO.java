package com.sw.web.domain;

public class CodeDetailVO {
	private int id;
	private String reprec;
	private String detailc;
	private String detailc_n;
	private String use;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getReprec() {
		return reprec;
	}
	public void setReprec(String reprec) {
		this.reprec = reprec;
	}
	public String getDetailc() {
		return detailc;
	}
	public void setDetailc(String detailc) {
		this.detailc = detailc;
	}
	public String getDetailc_n() {
		return detailc_n;
	}
	public void setDetailc_n(String detailc_n) {
		this.detailc_n = detailc_n;
	}
	public String getUse() {
		return use;
	}
	public void setUse(String use) {
		this.use = use;
	}
	
	
	

}
